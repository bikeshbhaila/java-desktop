    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int status = bc.withdraw(Integer.parseInt(txtFieldAccNo.getText()), Integer.parseInt(txtFieldAmount.getText()));
        switch(status) {
            case 1:
                JOptionPane.showMessageDialog(this, "Amount withdraw successfull!!!", "Withdraw Success", JOptionPane.OK_OPTION);
                this.dispose();
                break;
            case -1:
                JOptionPane.showMessageDialog(this, "The balance in the account is less than withdrawn amount.", "Insufficient balance", JOptionPane.OK_OPTION);
                txtFieldAmount.setText("");
                txtFieldAmount.grabFocus();
                break;
            case -2:
                JOptionPane.showMessageDialog(this, "Problem occured when executing query.", "SQL Failure", JOptionPane.OK_OPTION);
                this.dispose();
                break;
            case 0:
                JOptionPane.showMessageDialog(this, "The account you are trying to withdraw from doesn't exist.", "Account Not Found", JOptionPane.OK_OPTION);
                txtFieldAccNo.setText("");
                txtFieldAccNo.grabFocus();
                break;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

